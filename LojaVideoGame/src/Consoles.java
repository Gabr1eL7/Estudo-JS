
public class Consoles extends Cadastrar{
	

	private String marca;
	private String cor;
	private int tamanhoMemoria;
	private String estadoConsole;
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getTamanhoMemoria() {
		return tamanhoMemoria;
	}
	public void setTamanhoMemoria(int tamanhoMemoria) {
		this.tamanhoMemoria = tamanhoMemoria;
	}
	public String getEstadoConsole() {
		return estadoConsole;
	}
	public void setEstadoConsole(String estadoConsole) {
		this.estadoConsole = estadoConsole;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}

}
