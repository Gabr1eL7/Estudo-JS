
public class Jogos extends Cadastrar{
	
	private String desenvolvedora;
	private int classeIndicativa;
	private String plataforma;
	private String categoria;
	
	public String getDesenvolvedora() {
		return desenvolvedora;
	}
	public void setDesenvolvedora(String desenvolvedora) {
		this.desenvolvedora = desenvolvedora;
	}
	public int getClasseIndicativa() {
		return classeIndicativa;
	}
	public void setClasseIndicativa(int classeIndicativa) {
		this.classeIndicativa = classeIndicativa;
	}
	public String getPlataforma() {
		return plataforma;
	}
	public void setPlataforma(String plataforma) {
		this.plataforma = plataforma;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	} 

}
